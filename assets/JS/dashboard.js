document.addEventListener('DOMContentLoaded', () => {
    const languageDropdown = document.getElementById('language-dropdown');
    const currentLangDisplay = document.getElementById('current-language');
    const links = document.querySelectorAll('.dropdown-item');

    // Function to load the language (default to EN)
    function loadLanguage(lang) {
        document.querySelectorAll('[data-translate-key]').forEach(element => {
            const key = element.getAttribute('data-translate-key');
            if (translations[lang] && translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
        currentLangDisplay.textContent = lang;
        // Store the selected language
        localStorage.setItem('selectedLang', lang);
    }

    // Set initial language (from storage or default to EN)
    const initialLang = localStorage.getItem('selectedLang') || 'EN';
    loadLanguage(initialLang);

    // Add click event listener to language links
    links.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = e.target.getAttribute('data-lang');
            loadLanguage(lang);
        });
    });
});